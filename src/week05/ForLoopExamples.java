package week05;

public class ForLoopExamples {
    public static void main(String[] args) {
        for (int i = 0; i < 10; i++) {
            System.out.println("Hello");
            System.out.println("i = " + i);
        }

        System.out.println("***********************************************");
        for (int i = 1; i <= 5; i++) {
            System.out.println("hi - " + i);
        }
        System.out.println("=================================================");
        for (int i = 6; i >= 1; i--) {
            System.out.println("Bye " + i);
        }


    }
}
