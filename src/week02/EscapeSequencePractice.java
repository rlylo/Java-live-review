package week02;
/*
This is multiline comment:
    Escape sequences:
    \n -> adds new Line
    \t -> adds tab
    \" -> add double quote
    \' -> add single quote
    \\ -> add one backslash


 */

public class EscapeSequencePractice {
    public static void main(String[] args) {
        System.out.println("Line1\nLine2"); // New line
        System.out.println("Column1\tColumn2"); // Tab space
        System.out.println("Displaying a backslash: \\"); // Backslash
        System.out.println("Displaying a single quote: \'"); // Single quote
        System.out.println("Displaying a double quote: \""); // Double quote
        System.out.println("Carriage return example: Hello\rWorld"); // Carriage return
        System.out.println("Backspace example: Hello\bWorld!"); // Backspace
        System.out.println("Form feed example: Hello\fWorld!"); // Form feed

        System.out.println("-------------------------------------------------------");
//        Java SDET
        System.out.println("Jave\tSDET");

        //Hello Cydeo "Batch 37" friends!
        System.out.println("Hello Cydeo \"Batch 37\" friends!");

        //Let's code "java" everyday
        System.out.println("Let\'s code \"java\" everyday");
        System.out.println("Let's code \"java\" everyday");

        //to print \ I "have" to use \\ in Java
        // \_/
        System.out.println("\\_/");
        System.out.println("to print \\ I \"have\" to use \\\\ in Java");










    }
}
