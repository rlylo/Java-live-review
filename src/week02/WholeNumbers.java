package week02;

public class WholeNumbers {
    public static void main(String[] args) {
        int student = 200;
        System.out.println(student);

        int teachers = 2;

        System.out.println(student/teachers);
        System.out.println("teachers = " + teachers);

        //byte, short, int, long
        byte n1 = 100;
        short n2 = 32100;
        int n3 = 23_654_788;
        long n4 = 64654564654564L;

        byte screenSize = 16;
        System.out.println(screenSize);
        //change value to 24
        screenSize = 24;
        System.out.println(screenSize);

        int count = 55;
        int bananas = count;
        count = 30;
        System.out.println(bananas);
        System.out.println(count);


    }
}
